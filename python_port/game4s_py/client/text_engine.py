class TextEngine:
    def __init__(self) -> None:
        self.backend = "none"
        self._freetype = None

    def make_font(self, size: int):
        if self.backend == "pygame-font":
            return pygame.font.SysFont("Arial", size)
        if self.backend == "pygame-freetype" and self._freetype is not None:
            return self._freetype.SysFont("Arial", size)
        return _NoopFont(size)

    def _normalize_bitmap_text(self, text: str) -> str:
        result = text.translate(_RU_TO_LAT).upper()
        return "".join(ch if ch in _BITMAP_GLYPHS else " " for ch in result)

    def _render_bitmap(self, font, text: str, color: tuple[int, int, int]) -> pygame.Surface:
        normalized = self._normalize_bitmap_text(text)
        pixel = max(1, int(getattr(font, "size", 18) / 10))
        glyph_w = 5 * pixel
        glyph_h = 7 * pixel
        spacing = max(1, pixel)
        width = max(2, len(normalized) * (glyph_w + spacing) + spacing)
        height = glyph_h + spacing * 2
        surface = pygame.Surface((width, height), pygame.SRCALPHA)
        x = spacing
        for char in normalized:
            glyph = _BITMAP_GLYPHS.get(char, _BITMAP_GLYPHS[" "])
            for row_index, row_bits in enumerate(glyph):
                for col_index, bit in enumerate(row_bits):
                    if bit == "1":
                        pygame.draw.rect(surface, color, pygame.Rect(x + col_index * pixel, spacing + row_index * pixel, pixel, pixel))
            x += glyph_w + spacing
        return surface

    def render(self, font, text: str, color: tuple[int, int, int]) -> pygame.Surface:
        if self.backend == "pygame-font":
            return font.render(text, True, color)
        if self.backend == "pygame-freetype" and self._freetype is not None:
            surface, _ = font.render(text, fgcolor=color)
            return surface
        return self._render_bitmap(font, text, color)


def build_text_engine() -> TextEngine:
    engine = TextEngine()
    try:
        import pygame.freetype as freetype

        freetype.init()
        _ = freetype.SysFont("Arial", 18)
        engine.backend = "pygame-freetype"
        engine._freetype = freetype
        return engine
    except Exception:
        pass

    try:
        pygame.font.init()
        _ = pygame.font.SysFont("Arial", 18)
        engine.backend = "pygame-font"
        return engine
    except Exception:
        engine.backend = "bitmap"
        return engine
